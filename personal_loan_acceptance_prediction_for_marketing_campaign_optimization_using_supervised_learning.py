# Define hyperparameter search space for pre-pruning the Decision Tree
max_depth_values = np.arange(2, 7, 2) # Maximum depth of the tree: [2, 4, 6]
max_leaf_nodes_values = [50, 75, 150, 250] # Maximum number of leaf nodes allowed
min_samples_split_values = [10, 30, 50, 70] # Minimum samples required to split a node

# Initialize variables to track the best model and its train-test F1 score difference
best_estimator = None
best_score_diff = float('inf')

# Grid search over all combinations of hyperparameters
for max_depth in max_depth_values:
  for max_leaf_nodes in max_leaf_nodes_values:
    for min_samples_split in min_samples_split_values:

      # Initialize and train the Decision Tree with current hyperparameter combination
      estimator = DecisionTreeClassifier(
          max_depth = max_depth,
          max_leaf_nodes = max_leaf_nodes,
          min_samples_split = min_samples_split,
          random_state = 42
      )

      estimator.fit(X_train, y_train)

      # Generate predictions on both training and test sets
      y_train_pred = estimator.predict(X_train)
      y_test_pred = estimator.predict(X_test)

      # Compute F1 scores for both sets
      train_f1_score = f1_score(y_train, y_train_pred)
      test_f1_score = f1_score(y_test, y_test_pred)

      # Calculate the absolute difference between train and test F1 scores
      # A smaller difference indicates a better generalized model
      score_diff = abs(train_f1_score - test_f1_score)

      # Update the best estimator if current model has a smaller train-test gap
      if score_diff < best_score_diff:
        best_score_diff = score_diff
        best_estimator = estimator

print('Best Estimator:', best_estimator)
print('Best Score Difference:', best_score_diff)

